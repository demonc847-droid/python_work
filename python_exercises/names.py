# Create a list containing 10 person names
# In Python, a list is a collection of items that can be accessed by their index position
names = [
    "Alice Johnson",    # Index 0: First name in the list
    "Bob Smith",        # Index 1: Second name in the list
    "Charlie Brown",    # Index 2: Third name in the list
    "Diana Prince",     # Index 3: Fourth name in the list
    "Eve Wilson",       # Index 4: Fifth name in the list
    "Frank Miller",     # Index 5: Sixth name in the list
    "Grace Lee",        # Index 6: Seventh name in the list
    "Henry Davis",      # Index 7: Eighth name in the list
    "Ivy Thompson",     # Index 8: Ninth name in the list
    "Jack Anderson"     # Index 9: Tenth name in the list
]

# Print each name individually using its index number
# In Python, list indices start at 0, so the first item is at index 0,
# the second item is at index 1, and so on
print(f"Name 1: {names[0]}")  # Prints the first name: "Alice Johnson"
print(f"Name 2: {names[1]}")  # Prints the second name: "Bob Smith"
print(f"Name 3: {names[2]}")  # Prints the third name: "Charlie Brown"
